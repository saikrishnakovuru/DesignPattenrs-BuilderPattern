package myCampusTour.pojo;

public class StudentActivityDetails {
    private double caloriesSpent;
    private double durationOfActivity;
    private double costForActivity;
    private double carbonFootPrintMeasureOfCo2;

    public double getCaloriesSpent() {
        return caloriesSpent;
    }

    public void setCaloriesSpent(double caloriesSpent) {
        this.caloriesSpent = caloriesSpent;
    }

    public double getDurationOfActivity() {
        return durationOfActivity;
    }

    public void setDurationOfActivity(double durationOfActivity) {
        this.durationOfActivity = durationOfActivity;
    }

    public double getCostForActivity() {
        return costForActivity;
    }

    public void setCostForActivity(double costForActivity) {
        this.costForActivity = costForActivity;
    }

    public double getCarbonFootPrintMeasureOfCo2() {
        return carbonFootPrintMeasureOfCo2;
    }

    public void setCarbonFootPrintMeasureOfCo2(double carbonFootPrintMeasureOfCo2) {
        this.carbonFootPrintMeasureOfCo2 = carbonFootPrintMeasureOfCo2;
    }

}
